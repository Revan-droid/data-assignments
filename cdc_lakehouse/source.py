    def introduce_breaking_schema_change(self) -> None:
        with self.conn.transaction():
            self.conn.execute(f"ALTER TABLE {SCHEMA}.products RENAME COLUMN unit_price TO price_amount")

    def insert_customer(self, *, external_customer_ref: str, email: str, full_name: str, phone: str | None, customer_status: str, created_at: str | None = None) -> int:
        if customer_status not in ALLOWED_CUSTOMER_STATUSES:
            raise ValidationError(f"invalid customer_status: {customer_status}")
        now = created_at or utc_now_iso()
        row = self.conn.query(
            f"""
            INSERT INTO {SCHEMA}.customers (external_customer_ref, email, full_name, phone, customer_status, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?)
            RETURNING customer_id
            """,
            (external_customer_ref, email, full_name, phone, customer_status, now, now),
        ).fetchone()
        customer_id = int(row["customer_id"])
        after = self._row("customers", "customer_id = ?", [customer_id])
        self._record_change(table_name="customers", op="I", entity_key={"customer_id": customer_id}, before=None, after=after)
        return customer_id

    def insert_address(self, *, customer_id: int, address_type: str, line1: str, line2: str | None, city: str, state: str, postal_code: str, country_code: str, is_default: bool, created_at: str | None = None) -> int:
        if address_type not in ALLOWED_ADDRESS_TYPES:
            raise ValidationError(f"invalid address_type: {address_type}")
        now = created_at or utc_now_iso()
        row = self.conn.query(
            f"""
            INSERT INTO {SCHEMA}.customer_addresses (customer_id, address_type, line1, line2, city, state, postal_code, country_code, is_default, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            RETURNING address_id
            """,
            (customer_id, address_type, line1, line2, city, state, postal_code, country_code, 1 if is_default else 0, now, now),
        ).fetchone()
        address_id = int(row["address_id"])
        after = self._row("customer_addresses", "address_id = ?", [address_id])
        self._record_change(table_name="customer_addresses", op="I", entity_key={"address_id": address_id}, before=None, after=after)
        return address_id

    def insert_product(self, sku: str, product_name: str, category: str, unit_price: float, product_status: str, created_at: str | None = None) -> int:
        if product_status not in ALLOWED_PRODUCT_STATUSES:
            raise ValidationError(f"invalid product_status: {product_status}")
        if unit_price < 0:
            raise ValidationError("unit_price cannot be negative")
        now = created_at or utc_now_iso()
        row = self.conn.query(
            f"""
            INSERT INTO {SCHEMA}.products (sku, product_name, category, unit_price, product_status, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?)
            RETURNING product_id
            """,
            (sku, product_name, category, unit_price, product_status, now, now),
        ).fetchone()
        product_id = int(row["product_id"])
        after = self._row("products", "product_id = ?", [product_id])
        self._record_change(table_name="products", op="I", entity_key={"product_id": product_id}, before=None, after=after)
        return product_id

    def insert_order(self, *, customer_id: int, order_number: str, order_status: str, currency_code: str, subtotal: float, tax_amount: float, shipping_amount: float, total_amount: float, placed_at: str, cancelled_at: str | None, billing_address_id: int | None, shipping_address_id: int | None, notes: str | None, created_at: str | None = None) -> int:
        if order_status not in ALLOWED_ORDER_STATUSES:
            raise ValidationError(f"invalid order_status: {order_status}")
        if len(currency_code) != 3:
            raise ValidationError("currency_code must be ISO-4217-like")
        if min(subtotal, tax_amount, shipping_amount, total_amount) < 0:
            raise ValidationError("order monetary fields cannot be negative")
        now = created_at or utc_now_iso()
        row = self.conn.query(
            f"""
            INSERT INTO {SCHEMA}.orders (
                customer_id, order_number, order_status, currency_code, subtotal, tax_amount, shipping_amount,
                total_amount, placed_at, cancelled_at, billing_address_id, shipping_address_id, notes, created_at, updated_at
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            RETURNING order_id
            """,
            (customer_id, order_number, order_status, currency_code, subtotal, tax_amount, shipping_amount, total_amount, placed_at, cancelled_at, billing_address_id, shipping_address_id, notes, now, now),
        ).fetchone()
        order_id = int(row["order_id"])
        after = self._row("orders", "order_id = ?", [order_id])
        self._record_change(table_name="orders", op="I", entity_key={"order_id": order_id}, before=None, after=after)
        return order_id

    def insert_order_item(self, order_id: int, line_number: int, product_id: int, quantity: int, unit_price: float, discount_amount: float, created_at: str | None = None) -> None:
        if quantity <= 0:
            raise ValidationError("quantity must be positive")
        if unit_price < 0 or discount_amount < 0:
            raise ValidationError("prices cannot be negative")
        now = created_at or utc_now_iso()
        line_total = round(quantity * unit_price - discount_amount, 2)
        if line_total < 0:
            raise ValidationError("line_total cannot be negative")
        self.conn.execute(
            f"""
            INSERT INTO {SCHEMA}.order_items (
                order_id, line_number, product_id, quantity, unit_price, discount_amount, line_total, created_at, updated_at
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (order_id, line_number, product_id, quantity, unit_price, discount_amount, line_total, now, now),
        )
        after = self._row("order_items", "order_id = ? AND line_number = ?", [order_id, line_number])
        self._record_change(table_name="order_items", op="I", entity_key={"order_id": order_id, "line_number": line_number}, before=None, after=after)

    def finalize_order(self, order_id: int, *, tax_amount: float, shipping_amount: float, status: str) -> None:
        if status not in {"placed", "paid"}:
            raise ValidationError("finalized orders must move to placed or paid")
        before = self._row("orders", "order_id = ?", [order_id])
        if before is None:
            raise ValidationError(f"order_id {order_id} not found")
        allowed = {"draft": {"placed", "cancelled"}, "placed": {"paid", "cancelled"}, "paid": {"shipped", "refunded"}, "shipped": {"completed", "refunded"}, "completed": set(), "cancelled": set(), "refunded": set()}
        if status not in allowed[before["order_status"]]:
            raise ValidationError(f"invalid order transition {before['order_status']} -> {status}")
        items_total = self.conn.scalar(f"SELECT COALESCE(SUM(line_total), 0) FROM {SCHEMA}.order_items WHERE order_id = ?", (order_id,), default=0)
        total_amount = round(float(items_total) + tax_amount + shipping_amount, 2)
        now = utc_now_iso()
        cancelled_at = now if status == "cancelled" else before["cancelled_at"]
        self.conn.execute(
            f"""
            UPDATE {SCHEMA}.orders
            SET order_status = ?, subtotal = ?, tax_amount = ?, shipping_amount = ?, total_amount = ?, cancelled_at = ?, updated_at = ?
            WHERE order_id = ?
            """,
            (status, round(float(items_total), 2), round(tax_amount, 2), round(shipping_amount, 2), total_amount, cancelled_at, now, order_id),
        )
        after = self._row("orders", "order_id = ?", [order_id])
        self._record_change(table_name="orders", op="U", entity_key={"order_id": order_id}, before=before, after=after)

