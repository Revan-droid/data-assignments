SCHEMA = "source"

ALLOWED_CUSTOMER_STATUSES = {"active", "suspended", "closed"}
ALLOWED_PRODUCT_STATUSES = {"active", "discontinued"}
ALLOWED_ORDER_STATUSES = {"draft", "placed", "paid", "shipped", "completed", "cancelled", "refunded"}
ALLOWED_PAYMENT_STATUSES = {"pending", "authorized", "captured", "failed", "refunded"}
ALLOWED_ATTEMPT_STATUSES = {"started", "succeeded", "failed"}
ALLOWED_PAYMENT_METHODS = {"card", "bank_transfer", "wallet"}
ALLOWED_ADDRESS_TYPES = {"billing", "shipping"}


def _f(value: Any) -> float:
    return float(value) if value is not None else 0.0


def _i(value: Any) -> int:
    return int(value) if value is not None else 0


class SourceDatabase:
    def __init__(self, conn: PostgresConnection):
        self.conn = conn

    def close(self) -> None:
        return

    @staticmethod
    def tracked_tables() -> tuple[str, ...]:
        return ("customers", "customer_addresses", "products", "orders", "order_items", "payments", "payment_attempts")

    def initialize(self) -> None:
        with self.conn.transaction():
            self.conn.execute(f"CREATE SCHEMA IF NOT EXISTS {SCHEMA}")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.customers (
                    customer_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    external_customer_ref TEXT NOT NULL UNIQUE,
                    email TEXT NOT NULL UNIQUE,
                    full_name TEXT NOT NULL,
                    phone TEXT,
                    customer_status TEXT NOT NULL CHECK (customer_status IN ('active','suspended','closed')),
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_customers_status ON {SCHEMA}.customers(customer_status)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.customer_addresses (
                    address_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    customer_id BIGINT NOT NULL REFERENCES {SCHEMA}.customers(customer_id) ON DELETE CASCADE ON UPDATE CASCADE,
                    address_type TEXT NOT NULL CHECK (address_type IN ('billing','shipping')),
                    line1 TEXT NOT NULL,
                    line2 TEXT,
                    city TEXT NOT NULL,
                    state TEXT NOT NULL,
                    postal_code TEXT NOT NULL,
                    country_code TEXT NOT NULL,
                    is_default INTEGER NOT NULL CHECK (is_default IN (0,1)),
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL,
                    UNIQUE(customer_id, address_type, line1, postal_code)
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_customer_addresses_customer ON {SCHEMA}.customer_addresses(customer_id, is_default)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.products (
                    product_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    sku TEXT NOT NULL UNIQUE,
                    product_name TEXT NOT NULL,
                    category TEXT NOT NULL,
                    unit_price NUMERIC NOT NULL CHECK (unit_price >= 0),
                    product_status TEXT NOT NULL CHECK (product_status IN ('active','discontinued')),
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_products_status ON {SCHEMA}.products(product_status)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.orders (
                    order_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    customer_id BIGINT NOT NULL REFERENCES {SCHEMA}.customers(customer_id) ON DELETE RESTRICT ON UPDATE CASCADE,
                    order_number TEXT NOT NULL UNIQUE,
                    order_status TEXT NOT NULL CHECK (order_status IN ('draft','placed','paid','shipped','completed','cancelled','refunded')),
                    currency_code TEXT NOT NULL CHECK (length(currency_code) = 3),
                    subtotal NUMERIC NOT NULL CHECK (subtotal >= 0),
                    tax_amount NUMERIC NOT NULL CHECK (tax_amount >= 0),
                    shipping_amount NUMERIC NOT NULL CHECK (shipping_amount >= 0),
                    total_amount NUMERIC NOT NULL CHECK (total_amount >= 0),
                    placed_at TIMESTAMPTZ NOT NULL,
                    cancelled_at TIMESTAMPTZ,
                    billing_address_id BIGINT REFERENCES {SCHEMA}.customer_addresses(address_id) ON DELETE SET NULL ON UPDATE CASCADE,
                    shipping_address_id BIGINT REFERENCES {SCHEMA}.customer_addresses(address_id) ON DELETE SET NULL ON UPDATE CASCADE,
                    notes TEXT,
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_orders_customer_status ON {SCHEMA}.orders(customer_id, order_status, placed_at)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.order_items (
                    order_id BIGINT NOT NULL REFERENCES {SCHEMA}.orders(order_id) ON DELETE CASCADE ON UPDATE CASCADE,
                    line_number INTEGER NOT NULL,
                    product_id BIGINT NOT NULL REFERENCES {SCHEMA}.products(product_id) ON DELETE RESTRICT ON UPDATE CASCADE,
                    quantity INTEGER NOT NULL CHECK (quantity > 0),
                    unit_price NUMERIC NOT NULL CHECK (unit_price >= 0),
                    discount_amount NUMERIC NOT NULL DEFAULT 0 CHECK (discount_amount >= 0),
                    line_total NUMERIC NOT NULL CHECK (line_total >= 0),
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL,
                    PRIMARY KEY (order_id, line_number)
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_order_items_product ON {SCHEMA}.order_items(product_id)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.payments (
                    payment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    order_id BIGINT NOT NULL REFERENCES {SCHEMA}.orders(order_id) ON DELETE RESTRICT ON UPDATE CASCADE,
                    payment_method TEXT NOT NULL CHECK (payment_method IN ('card','bank_transfer','wallet')),
                    payment_status TEXT NOT NULL CHECK (payment_status IN ('pending','authorized','captured','failed','refunded')),
                    amount NUMERIC NOT NULL CHECK (amount > 0),
                    provider_reference TEXT UNIQUE,
                    authorized_at TIMESTAMPTZ,
                    captured_at TIMESTAMPTZ,
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_payments_order_status ON {SCHEMA}.payments(order_id, payment_status)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.payment_attempts (
                    payment_attempt_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    payment_id BIGINT NOT NULL REFERENCES {SCHEMA}.payments(payment_id) ON DELETE CASCADE ON UPDATE CASCADE,
                    attempt_no INTEGER NOT NULL CHECK (attempt_no > 0),
                    attempt_status TEXT NOT NULL CHECK (attempt_status IN ('started','succeeded','failed')),
                    decline_code TEXT,
                    gateway_latency_ms INTEGER CHECK (gateway_latency_ms IS NULL OR gateway_latency_ms >= 0),
                    attempted_at TIMESTAMPTZ NOT NULL,
                    request_id TEXT NOT NULL UNIQUE,
                    created_at TIMESTAMPTZ NOT NULL,
                    updated_at TIMESTAMPTZ NOT NULL,
                    UNIQUE(payment_id, attempt_no)
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_payment_attempts_payment ON {SCHEMA}.payment_attempts(payment_id, attempted_at)")
            self.conn.execute(
                f"""
                CREATE TABLE IF NOT EXISTS {SCHEMA}.cdc_log (
                    change_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    table_name TEXT NOT NULL,
                    op TEXT NOT NULL CHECK (op IN ('I','U','D')),
                    entity_key JSONB NOT NULL,
                    source_commit_ts TIMESTAMPTZ NOT NULL,
                    source_txn_id TEXT NOT NULL,
                    before_json JSONB,
                    after_json JSONB,
                    source_schema_hash TEXT NOT NULL
                )
                """
            )
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_cdc_log_change_id ON {SCHEMA}.cdc_log(change_id)")
            self.conn.execute(f"CREATE INDEX IF NOT EXISTS idx_cdc_log_table_change_id ON {SCHEMA}.cdc_log(table_name, change_id)")

    def _raw_row(self, table: str, where_sql: str, params: Iterable[Any]) -> dict[str, Any] | None:
        result = self.conn.query(f"SELECT * FROM {SCHEMA}.{table} WHERE {where_sql} LIMIT 1", tuple(params))
        return result.fetchone()

    def _normalize(self, table: str, row: dict[str, Any] | None) -> dict[str, Any] | None:
        if row is None:
            return None
        converted = dict(row)
        for key in list(converted):
            if key in {"customer_id", "address_id", "product_id", "order_id", "payment_id", "payment_attempt_id", "line_number", "quantity", "attempt_no", "gateway_latency_ms"}:
                converted[key] = None if converted[key] is None else int(converted[key])
            if key in {"unit_price", "discount_amount", "line_total", "subtotal", "tax_amount", "shipping_amount", "total_amount", "amount"}:
                converted[key] = None if converted[key] is None else float(converted[key])
            if key in {"is_default"}:
                converted[key] = int(converted[key])
        return converted

    def _row(self, table: str, where_sql: str, params: Iterable[Any]) -> dict[str, Any] | None:
        return self._normalize(table, self._raw_row(table, where_sql, params))

    def schema_contract(self) -> TableContract:
        columns: list[SchemaColumn] = []
        foreign_keys: list[tuple[str, str, str]] = []
        indexes: list[str] = []
        table_names = ", ".join(quote for quote in [f"'{t}'" for t in self.tracked_tables()])
        for row in self.conn.query(
            f"""
            SELECT table_name, column_name, data_type, is_nullable, column_default, ordinal_position
            FROM information_schema.columns
            WHERE table_schema = '{SCHEMA}'
              AND table_name IN ({table_names})
            ORDER BY table_name, ordinal_position
            """
        ).fetchall():
            columns.append(
                SchemaColumn(
                    table_name=row["table_name"],
                    column_name=row["column_name"],
                    data_type=row["data_type"],
                    notnull=row["is_nullable"] == "NO",
                    is_pk=False,
                    default_value=row["column_default"],
                    ordinal=int(row["ordinal_position"]),
                )
            )
        pk_rows = self.conn.query(
            f"""
            SELECT kcu.table_name, kcu.column_name
            FROM information_schema.table_constraints tc
            JOIN information_schema.key_column_usage kcu
              ON tc.constraint_name = kcu.constraint_name
             AND tc.table_schema = kcu.table_schema
            WHERE tc.table_schema = '{SCHEMA}' AND tc.constraint_type = 'PRIMARY KEY'
            ORDER BY kcu.table_name, kcu.ordinal_position
            """
        ).fetchall()
        pk_map = {(row["table_name"], row["column_name"]) for row in pk_rows}
        columns = [
            SchemaColumn(
                table_name=c.table_name,
                column_name=c.column_name,
                data_type=c.data_type,
                notnull=c.notnull,
                is_pk=(c.table_name, c.column_name) in pk_map,
                default_value=c.default_value,
                ordinal=c.ordinal,
            )
            for c in columns
        ]
        for row in self.conn.query(
            f"""
            SELECT tc.table_name, kcu.column_name, ccu.table_name AS foreign_table, ccu.column_name AS foreign_column
            FROM information_schema.table_constraints tc
            JOIN information_schema.key_column_usage kcu
              ON tc.constraint_name = kcu.constraint_name
             AND tc.table_schema = kcu.table_schema
            JOIN information_schema.constraint_column_usage ccu
              ON tc.constraint_name = ccu.constraint_name
             AND tc.table_schema = ccu.table_schema
            WHERE tc.table_schema = '{SCHEMA}' AND tc.constraint_type = 'FOREIGN KEY'
            ORDER BY tc.table_name, kcu.ordinal_position
            """
        ).fetchall():
            foreign_keys.append((row["table_name"], row["column_name"], f"{row['foreign_table']}.{row['foreign_column']}"))
        for row in self.conn.query(
            f"""
            SELECT indexname, indexdef
            FROM pg_indexes
            WHERE schemaname = '{SCHEMA}'
            ORDER BY indexname
            """
        ).fetchall():
            indexes.append(f"{row['indexname']}:{row['indexdef']}")
        return TableContract(table_name=f"{SCHEMA}.contract", columns=tuple(columns), foreign_keys=tuple(foreign_keys), indexes=tuple(indexes))

    def schema_fingerprint(self) -> str:
        contract = self.schema_contract()
        payload = {
            "columns": [
                {
                    "table_name": c.table_name,
                    "column_name": c.column_name,
                    "data_type": c.data_type,
                    "notnull": c.notnull,
                    "is_pk": c.is_pk,
                    "default_value": c.default_value,
                    "ordinal": c.ordinal,
                }
                for c in contract.columns
            ],
            "foreign_keys": list(contract.foreign_keys),
            "indexes": sorted(contract.indexes),
        }
        return sha256_text(stable_json(payload))

    def _record_change(self, *, table_name: str, op: str, entity_key: dict[str, Any], before: dict[str, Any] | None, after: dict[str, Any] | None) -> int:
        commit_ts = utc_now_iso()
        txn_id = f"{table_name}:{op}:{commit_ts}:{stable_json(entity_key)}"
        row = self.conn.query(
            f"""
            INSERT INTO {SCHEMA}.cdc_log (
                table_name, op, entity_key, source_commit_ts, source_txn_id, before_json, after_json, source_schema_hash
            ) VALUES (?, ?, ?::jsonb, ?, ?, ?::jsonb, ?::jsonb, ?)
            RETURNING change_id
            """,
            (
                table_name,
                op,
                stable_json(entity_key),
                commit_ts,
                txn_id,
                None if before is None else stable_json(before),
                None if after is None else stable_json(after),
                self.schema_fingerprint(),
            ),
        ).fetchone()
        return int(row["change_id"])

    def fetch_changes_since(self, change_id: int) -> list[ChangeEvent]:
        def _normalize_json(value: Any) -> dict[str, Any] | None:
            if value is None:
                return None
            if isinstance(value, dict):
                return value
            if isinstance(value, str):
                return json.loads(value)
            raise TypeError(f"unsupported JSON value type: {type(value).__name__}")

        rows = self.conn.query(
            f"""
            SELECT change_id, table_name, op, entity_key, source_commit_ts, source_txn_id, before_json, after_json, source_schema_hash
            FROM {SCHEMA}.cdc_log
            WHERE change_id > ?
            ORDER BY change_id
            """,
            (change_id,),
        ).fetchall()
        events: list[ChangeEvent] = []
        for row in rows:
            events.append(
                ChangeEvent(
                    change_id=int(row["change_id"]),
                    table_name=row["table_name"],
                    op=row["op"],
                    entity_key=_normalize_json(row["entity_key"]) or {},
                    source_commit_ts=row["source_commit_ts"],
                    source_txn_id=row["source_txn_id"],
                    before=_normalize_json(row["before_json"]),
                    after=_normalize_json(row["after_json"]),
                    source_schema_hash=row["source_schema_hash"],
                )
            )
        return events

